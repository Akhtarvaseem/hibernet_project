package com.jsp.jdbc_crud_with_statement.dto;

public class Custmer {
	
	private int custmerId;
	private String custmerName;
	private String custmerEmail;
	
	public int getCustmerId() {
		return custmerId;
	}
	public void setCustmerId(int custmerId) {
		this.custmerId = custmerId;
	}
	public String getCustmerName() {
		return custmerName;
	}
	public void setCustmerName(String custmerName) {
		this.custmerName = custmerName;
	}
	public String getCustmerEmail() {
		return custmerEmail;
	}
	public void setCustmerEmail(String custmerEmail) {
		this.custmerEmail = custmerEmail;
	}
	

}
